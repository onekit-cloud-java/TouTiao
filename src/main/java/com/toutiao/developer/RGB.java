package com.toutiao.developer;



public class RGB  {
     private Integer r, g, b;

     public RGB(Integer r, Integer g, Integer b) {
          this.r = r;
          this.g = g;
          this.b = b;
     }

     public Integer getR() {
          return r;
     }

     public Integer getG() {
          return g;
     }


     public Integer getB() {
          return b;
     }

}
